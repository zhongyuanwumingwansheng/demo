package com.example.demo.model;

public class ParseResult {
    String instId;

    String inputProject;

    String inputTable;

    String inputColumn;

    String outputProject;

    String outputTable;

    String outputColumn;

    String handleType;

    String place;

    public String getInstId() {
        return instId;
    }

    public void setInstId(String instId) {
        this.instId = instId;
    }

    public String getInputProject() {
        return inputProject;
    }

    public void setInputProject(String inputProject) {
        this.inputProject = inputProject;
    }

    public String getInputTable() {
        return inputTable;
    }

    public void setInputTable(String inputTable) {
        this.inputTable = inputTable;
    }

    public String getInputColumn() {
        return inputColumn;
    }

    public void setInputColumn(String inputColumn) {
        this.inputColumn = inputColumn;
    }

    public String getOutputProject() {
        return outputProject;
    }

    public void setOutputProject(String outputProject) {
        this.outputProject = outputProject;
    }

    public String getOutputTable() {
        return outputTable;
    }

    public void setOutputTable(String outputTable) {
        this.outputTable = outputTable;
    }

    public String getOutputColumn() {
        return outputColumn;
    }

    public void setOutputColumn(String outputColumn) {
        this.outputColumn = outputColumn;
    }

    public String getHandleType() {
        return handleType;
    }

    public void setHandleType(String handleType) {
        this.handleType = handleType;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }
}
